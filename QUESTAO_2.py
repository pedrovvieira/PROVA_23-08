#Criando a classe
class Pessoa:

	def __init__(self,nome,idade,peso,altura):
		self.nome = nome
		self.idade=idade
		self.peso=peso
		self.altura=altura

#Alterar idade
	def envelhecer(self,idade):
		self.idade=idade

#Alterar peso engordando
	def engordar(self,peso):
		self.peso=engordar

#Alterar peso emagrecendo
	def emagrecer(self,peso):
		self.peso=emagrecer

#Alterar a altura
	def crecer(self,altura):
		self.altura=crecer

